#!/usr/bin/env python3
"""Collect competitor benchmark results in Graphene-compatible logs format.

This script runs available competitor analyzers against Graphene test suites and
writes session folders that match the `results.csv` schema consumed by
`analyze_performance.py`.
"""

from __future__ import annotations

import argparse
import csv
import re
import shutil
import subprocess
import time
import uuid
from dataclasses import dataclass
from datetime import datetime
from pathlib import Path
from typing import Iterable

ROOT = Path(__file__).resolve().parent.parent


@dataclass
class CaseResult:
    target: str
    success: bool
    crashed: bool
    escape_detected: bool
    execution_time_ms: float
    error: str


def run_command(cmd: list[str], cwd: Path | None = None) -> tuple[int, str, str, float]:
    start = time.perf_counter()
    proc = subprocess.run(
        cmd,
        cwd=str(cwd) if cwd else None,
        capture_output=True,
        text=True,
        encoding="utf-8",
        errors="replace",
    )
    elapsed_ms = (time.perf_counter() - start) * 1000.0
    return proc.returncode, proc.stdout, proc.stderr, elapsed_ms


def session_name() -> str:
    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    return f"session_{ts}_{uuid.uuid4().hex[:8]}"


def write_session(log_root: Path, language_key: str, result: CaseResult) -> None:
    sdir = log_root / language_key / session_name()
    sdir.mkdir(parents=True, exist_ok=True)

    readme = sdir / "README.md"
    readme.write_text(
        "\n".join(
            [
                "# Competitor Benchmark Session",
                "",
                f"**Language:** `{language_key}`",
                f"**Target:** `{result.target}`",
                "",
                "Generated by scripts/collect_competitor_benchmarks.py",
            ]
        )
        + "\n",
        encoding="utf-8",
    )

    with (sdir / "results.csv").open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(
            f,
            fieldnames=[
                "input",
                "success",
                "crashed",
                "escape_detected",
                "execution_time_ms",
                "error",
            ],
        )
        writer.writeheader()
        writer.writerow(
            {
                "input": "benchmark",
                "success": str(result.success).lower(),
                "crashed": str(result.crashed).lower(),
                "escape_detected": str(result.escape_detected).lower(),
                "execution_time_ms": f"{result.execution_time_ms:.3f}",
                "error": result.error,
            }
        )


def collect_python_cases(limit: int) -> list[Path]:
    cases = sorted((ROOT / "tests" / "python" / "cases").glob("case_*.py"))
    return cases if limit <= 0 else cases[:limit]


def collect_js_cases(limit: int) -> list[Path]:
    cases = sorted((ROOT / "tests" / "nodejs" / "cases").glob("case_*.js"))
    return cases if limit <= 0 else cases[:limit]


def collect_go_cases(limit: int) -> list[Path]:
    cases = sorted((ROOT / "tests" / "go" / "cases").glob("case_*.go"))
    return cases if limit <= 0 else cases[:limit]


def collect_rust_cases(limit: int) -> list[Path]:
    cases = sorted((ROOT / "tests" / "rust" / "cases").glob("case_*.rs"))
    return cases if limit <= 0 else cases[:limit]


def collect_java_cases(limit: int) -> list[Path]:
    base = ROOT / "tests" / "java" / "src" / "main" / "java" / "com" / "escape" / "tests" / "cases"
    cases = sorted(base.glob("Case*.java"))
    return cases if limit <= 0 else cases[:limit]


def _has_escape_markers(text: str, patterns: list[str]) -> bool:
    return any(re.search(pattern, text, flags=re.MULTILINE) for pattern in patterns)


def run_python_escape_ast(py_cases: list[Path]) -> dict[Path, CaseResult]:
    results: dict[Path, CaseResult] = {}
    if not py_cases:
        return results

    patterns = [
        r"RETAINED_[A-Z_]+\s*\[",
        r"RETAINED_[A-Z_]+\.append\(",
        r"RETAINED_HANDLERS\.append\(",
        r"def\s+handler\s*\(",
    ]

    start = time.perf_counter()
    for p in py_cases:
        text = p.read_text(encoding="utf-8", errors="replace")
        escaped = _has_escape_markers(text, patterns)
        target = str(p.relative_to(ROOT)).replace("\\", "/")
        results[p] = CaseResult(target, True, False, escaped, 0.0, "")

    per_case_ms = ((time.perf_counter() - start) * 1000.0) / max(1, len(py_cases))
    for p in py_cases:
        results[p].execution_time_ms = per_case_ms

    return results


def run_node_escape_ast(js_cases: list[Path]) -> dict[Path, CaseResult]:
    results: dict[Path, CaseResult] = {}
    if not js_cases:
        return results

    patterns = [
        r"retained[A-Za-z]+\s*\.push\(",
        r"retainedHandlers\s*\.push\(",
        r"queueMicrotask\s*\(",
        r"setTimeout\s*\(",
    ]

    start = time.perf_counter()
    for p in js_cases:
        text = p.read_text(encoding="utf-8", errors="replace")
        escaped = _has_escape_markers(text, patterns)
        target = str(p.relative_to(ROOT)).replace("\\", "/")
        results[p] = CaseResult(target, True, False, escaped, 0.0, "")

    per_case_ms = ((time.perf_counter() - start) * 1000.0) / max(1, len(js_cases))
    for p in js_cases:
        results[p].execution_time_ms = per_case_ms

    return results


def run_go_native_escape(go_cases: list[Path]) -> dict[Path, CaseResult]:
    results: dict[Path, CaseResult] = {}
    if not go_cases:
        return results

    go = shutil.which("go")
    if not go:
        for p in go_cases:
            results[p] = CaseResult(str(p.relative_to(ROOT)).replace("\\", "/"), False, True, False, 0.0, "go executable missing for native escape analysis")
        return results

    go_cwd = ROOT / "tests" / "go"

    for p in go_cases:
        rel_file = Path("cases") / p.name
        cmd = [go, "build", "-gcflags=all=-m", f"./{rel_file.as_posix()}"]
        rc, out, err, elapsed = run_command(cmd, cwd=go_cwd)
        stream = "\n".join([out, err])
        escaped = "escapes to heap" in stream
        shared_error = (err or out).strip()[:600]

        results[p] = CaseResult(
            target=str(p.relative_to(ROOT)).replace("\\", "/"),
            success=rc == 0,
            crashed=rc != 0,
            escape_detected=escaped,
            execution_time_ms=elapsed,
            error=shared_error if rc != 0 else "",
        )

    return results


