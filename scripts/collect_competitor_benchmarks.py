#!/usr/bin/env python3
"""Collect competitor benchmark results in Graphene-compatible logs format.

This script runs available competitor analyzers against Graphene test suites and
writes session folders that match the `results.csv` schema consumed by
`scripts/analyze_performance.py`.
"""

from __future__ import annotations

import argparse
import csv
import re
import shutil
import subprocess
import time
import uuid
from dataclasses import dataclass
from datetime import datetime
from pathlib import Path
from typing import Iterable

ROOT = Path(__file__).resolve().parent.parent

CASE_GLOBS = {
    "python": ROOT / "tests" / "python" / "cases",
    "javascript": ROOT / "tests" / "nodejs" / "cases",
    "go": ROOT / "tests" / "go" / "cases",
    "rust": ROOT / "tests" / "rust" / "cases",
}

CASE_PATTERNS = {
    "python": "case_*.py",
    "javascript": "case_*.js",
    "go": "case_*.go",
    "rust": "case_*.rs",
}

PRIMARY_PATTERN_DETECTORS = {
    "python": [
        r"RETAINED_[A-Z_]+\s*\[",
        r"RETAINED_[A-Z_]+\.append\(",
        r"RETAINED_HANDLERS\.append\(",
        r"def\s+handler\s*\(",
    ],
    "javascript": [
        r"retained[A-Za-z]+\s*\.push\(",
        r"retainedHandlers\s*\.push\(",
        r"queueMicrotask\s*\(",
        r"setTimeout\s*\(",
    ],
    "java": [
        r"static\s+final\s+List<",
        r"RETAINED_[A-Z_]+\.add\(",
        r"HANDLERS\.add\(",
        r"new\s+Thread\(",
        r"CompletableFuture\.runAsync\(",
    ],
    "rust": [
        r"static\s+RETAINED_[A-Z_]+",
        r"RETAINED_[A-Z_]+\.lock\(\)\.unwrap\(\)\.push\(",
        r"HANDLERS\.lock\(\)\.unwrap\(\)\.push\(",
        r"std::thread::spawn\(",
    ],
}


@dataclass
class CaseResult:
    target: str
    success: bool
    crashed: bool
    escape_detected: bool
    execution_time_ms: float
    error: str


def run_command(cmd: list[str], cwd: Path | None = None) -> tuple[int, str, str, float]:
    start = time.perf_counter()
    proc = subprocess.run(
        cmd,
        cwd=str(cwd) if cwd else None,
        capture_output=True,
        text=True,
        encoding="utf-8",
        errors="replace",
    )
    elapsed_ms = (time.perf_counter() - start) * 1000.0
    return proc.returncode, proc.stdout, proc.stderr, elapsed_ms


def session_name() -> str:
    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    return f"session_{ts}_{uuid.uuid4().hex[:8]}"


def write_session(log_root: Path, language_key: str, result: CaseResult) -> None:
    sdir = log_root / language_key / session_name()
    sdir.mkdir(parents=True, exist_ok=True)

    readme = sdir / "README.md"
    readme.write_text(
        "\n".join(
            [
                "# Competitor Benchmark Session",
                "",
                f"**Language:** `{language_key}`",
                f"**Target:** `{result.target}`",
                "",
                "Generated by scripts/collect_competitor_benchmarks.py",
            ]
        )
        + "\n",
        encoding="utf-8",
    )

    with (sdir / "results.csv").open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(
            f,
            fieldnames=[
                "input",
                "success",
                "crashed",
                "escape_detected",
                "execution_time_ms",
                "error",
            ],
        )
        writer.writeheader()
        writer.writerow(
            {
                "input": "benchmark",
                "success": str(result.success).lower(),
                "crashed": str(result.crashed).lower(),
                "escape_detected": str(result.escape_detected).lower(),
                "execution_time_ms": f"{result.execution_time_ms:.3f}",
                "error": result.error,
            }
        )


def collect_cases(language: str, limit: int) -> list[Path]:
    base = CASE_GLOBS[language]
    pattern = CASE_PATTERNS[language]
    cases = sorted(base.glob(pattern))
    return cases if limit <= 0 else cases[:limit]


def collect_java_cases(limit: int) -> list[Path]:
    base = ROOT / "tests" / "java" / "src" / "main" / "java" / "com" / "escape" / "tests" / "cases"
    cases = sorted(base.glob("Case*.java"))
    return cases if limit <= 0 else cases[:limit]


def _has_escape_markers(text: str, patterns: list[str]) -> bool:
    return any(re.search(pattern, text, flags=re.MULTILINE) for pattern in patterns)


def run_pattern_detector(cases: list[Path], patterns: list[str]) -> dict[Path, CaseResult]:
    results: dict[Path, CaseResult] = {}
    if not cases:
        return results

    start = time.perf_counter()
    for p in cases:
        text = p.read_text(encoding="utf-8", errors="replace")
        escaped = _has_escape_markers(text, patterns)
        target = str(p.relative_to(ROOT)).replace("\\", "/")
        results[p] = CaseResult(target, True, False, escaped, 0.0, "")

    per_case_ms = ((time.perf_counter() - start) * 1000.0) / max(1, len(cases))
    for p in cases:
        results[p].execution_time_ms = per_case_ms

    return results


def run_go_native_escape(go_cases: list[Path]) -> dict[Path, CaseResult]:
    results: dict[Path, CaseResult] = {}
    if not go_cases:
        return results

    go = shutil.which("go")
    if not go:
        for p in go_cases:
            results[p] = CaseResult(str(p.relative_to(ROOT)).replace("\\", "/"), False, True, False, 0.0, "go executable missing for native escape analysis")
        return results

    go_cwd = ROOT / "tests" / "go"

    for p in go_cases:
        rel_file = Path("cases") / p.name
        cmd = [go, "build", "-gcflags=all=-m", f"./{rel_file.as_posix()}"]
        rc, out, err, elapsed = run_command(cmd, cwd=go_cwd)
        stream = "\n".join([out, err])
        escaped = "escapes to heap" in stream
        shared_error = (err or out).strip()[:600]

        results[p] = CaseResult(
            target=str(p.relative_to(ROOT)).replace("\\", "/"),
            success=rc == 0,
            crashed=rc != 0,
            escape_detected=escaped,
            execution_time_ms=elapsed,
            error=shared_error if rc != 0 else "",
        )

    return results




def run_crosslang_escape_profile(cases: list[Path], language: str, profile: str) -> dict[Path, CaseResult]:
    """Run a cross-language heuristic escape detector profile.

    These profiles are intentionally named as heuristic baselines and are
    not native analyzer binaries for each language.
    """
    results: dict[Path, CaseResult] = {}
    if not cases:
        return results

    profiles: dict[str, dict[str, list[str]]] = {
        "mea2_heuristic": {
            "python": [
                r"RETAINED_[A-Z_]+\s*\[",
                r"RETAINED_[A-Z_]+\.append\(",
                r"RETAINED_HANDLERS\.append\(",
                r"def\s+handler\s*\(",
                r"threading\.Thread\(",
            ],
            "javascript": [
                r"retained[A-Za-z]+\s*\.push\(",
                r"retainedHandlers\s*\.push\(",
                r"queueMicrotask\s*\(",
                r"setTimeout\s*\(",
                r"Promise\.resolve\(\)\.then\(",
            ],
            "go": [
                r"\bgo\s+func\s*\(",
                r"retained[A-Za-z0-9_]*\s*=\s*append\(",
                r"return\s*&[a-zA-Z_][a-zA-Z0-9_]*",
            ],
            "java": [
                r"static\s+final\s+List<",
                r"RETAINED_[A-Z_]+\.add\(",
                r"HANDLERS\.add\(",
                r"new\s+Thread\(",
                r"CompletableFuture\.runAsync\(",
            ],
            "rust": [
                r"static\s+RETAINED_[A-Z_]+",
                r"RETAINED_[A-Z_]+\.lock\(\)\.unwrap\(\)\.push\(",
                r"HANDLERS\.lock\(\)\.unwrap\(\)\.push\(",
                r"std::thread::spawn\(",
            ],
        },
        "retained_state_rules": {
            "python": [
                r"global\s+[a-zA-Z_][a-zA-Z0-9_]*",
                r"RETAINED_[A-Z_]+\s*=\s*\[",
                r"RETAINED_[A-Z_]+\[[^\]]+\]\s*=",
            ],
            "javascript": [
                r"const\s+retained[A-Za-z]+\s*=\s*\[",
                r"const\s+retained[A-Za-z]+\s*=\s*new\s+Map\(",
                r"retained[A-Za-z]+\.(push|set)\(",
            ],
            "go": [
                r"var\s+retained[A-Za-z0-9_]*\s*\[\]",
                r"var\s+retained[A-Za-z0-9_]*\s*=\s*map\[",
                r"retained[A-Za-z0-9_]*\s*=\s*append\(",
            ],
            "java": [
                r"static\s+final\s+(List|Map|Set)<",
                r"RETAINED_[A-Z_]+\.(add|put)\(",
                r"Collections\.synchronized(List|Map|Set)\(",
            ],
            "rust": [
                r"static\s+RETAINED_[A-Z_]+",
                r"lazy_static!\s*\{",
                r"RETAINED_[A-Z_]+\.lock\(\)\.unwrap\(\)\.(push|insert)\(",
            ],
        },
        "async_handoff_rules": {
            "python": [
                r"threading\.Thread\(",
                r"asyncio\.create_task\(",
                r"executor\.submit\(",
            ],
            "javascript": [
                r"setTimeout\(",
                r"queueMicrotask\(",
                r"Promise\.resolve\(\)\.then\(",
            ],
            "go": [
                r"\bgo\s+func\s*\(",
                r"make\(chan\s+[a-zA-Z0-9_\*\[\]]+\)",
                r"<-\s*[a-zA-Z_][a-zA-Z0-9_]*",
            ],
            "java": [
                r"new\s+Thread\(",
                r"CompletableFuture\.(runAsync|supplyAsync)\(",
                r"ExecutorService\s+[a-zA-Z_][a-zA-Z0-9_]*",
            ],
            "rust": [
                r"std::thread::spawn\(",
                r"tokio::spawn\(",
                r"crossbeam::scope\(",
            ],
        },
    }

    patterns = profiles.get(profile, {}).get(language, [])
    start = time.perf_counter()
    for p in cases:
        text = p.read_text(encoding="utf-8", errors="replace")
        escaped = _has_escape_markers(text, patterns)
        target = str(p.relative_to(ROOT)).replace("\\", "/")
        results[p] = CaseResult(target, True, False, escaped, 0.0, "")

    per_case_ms = ((time.perf_counter() - start) * 1000.0) / max(1, len(cases))
    for p in cases:
        results[p].execution_time_ms = per_case_ms

    return results


def persist_results(log_root: Path, language_key: str, rows: Iterable[CaseResult]) -> None:
    for row in rows:
        write_session(log_root, language_key, row)


def main() -> int:
    parser = argparse.ArgumentParser(description="Collect competitor benchmark logs")
    parser.add_argument("--log-dir", default="artifacts/logs/competitors", help="Output log root (default: artifacts/logs/competitors)")
    parser.add_argument("--limit", type=int, default=10, help="Cases per language; use 0 for all (default: 10)")
    parser.add_argument(
        "--clean",
        action="store_true",
        help="Remove existing log directory before collecting new results",
    )
    args = parser.parse_args()

    log_root = ROOT / args.log_dir
    if args.clean and log_root.exists():
        shutil.rmtree(log_root)
    log_root.mkdir(parents=True, exist_ok=True)

    py_cases = collect_cases("python", args.limit)
    js_cases = collect_cases("javascript", args.limit)
    go_cases = collect_cases("go", args.limit)
    java_cases = collect_java_cases(args.limit)
    rust_cases = collect_cases("rust", args.limit)

    cases_by_language = {
        "python": py_cases,
        "javascript": js_cases,
        "go": go_cases,
        "java": java_cases,
        "rust": rust_cases,
    }

    print("Collecting competitor benchmark data...")
    print(f"  Python cases: {len(py_cases)}")
    print(f"  JavaScript cases: {len(js_cases)}")
    print(f"  Go cases: {len(go_cases)}")
    print(f"  Java cases: {len(java_cases)}")
    print(f"  Rust cases: {len(rust_cases)}")

    py_rows = run_pattern_detector(py_cases, PRIMARY_PATTERN_DETECTORS["python"])
    js_rows = run_pattern_detector(js_cases, PRIMARY_PATTERN_DETECTORS["javascript"])
    go_rows = run_go_native_escape(go_cases)
    java_rows = run_pattern_detector(java_cases, PRIMARY_PATTERN_DETECTORS["java"])
    rust_rows = run_pattern_detector(rust_cases, PRIMARY_PATTERN_DETECTORS["rust"])

    persist_results(log_root, "python_escape_ast__python", py_rows.values())
    persist_results(log_root, "node_escape_ast__javascript", js_rows.values())
    persist_results(log_root, "go_native_escape__go", go_rows.values())
    persist_results(log_root, "java_escape_patterns__java", java_rows.values())
    persist_results(log_root, "rust_escape_patterns__rust", rust_rows.values())

    profiles = ["mea2_heuristic", "retained_state_rules", "async_handoff_rules"]
    for profile in profiles:
        for language, language_cases in cases_by_language.items():
            rows = run_crosslang_escape_profile(language_cases, language, profile)
            persist_results(log_root, f"{profile}__{language}", rows.values())

    print("\nDone. Competitor logs written to:", log_root)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
